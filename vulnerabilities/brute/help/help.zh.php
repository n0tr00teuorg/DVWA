		<h3>简介</h3>
		<p>密码破解是指从存储在计算机系统中或由计算机系统传输的数据中恢复密码的过程。
		一种常见的方法是反复尝试猜测密码。</p>

		<p>用户经常选择薄弱的密码。不安全选择的例子包括字典中的单字、姓氏、任何太短的密码（通常被认为是少于6或7个字符），
		或可预测的模式 (例如，字符与相似的符号或数字交替使用，这就是所谓的leet语，所以 “password” 变成了 “p@55w0rd”）。</p>

		<p>创建一个对目标具有针对性的爆破字典，往往能获得更高的成功率。
		有些开源工具可以根据公司网站、个人社交网络和其他常见信息（如生日或毕业年份）创建一个字典。</p>

		<p>最后的办法是尝试所有可能的密码，也就是所谓的暴力穷举。
		理论上，如果尝试次数限制，暴力穷举总是会成功的，因为可接受的密码规则必须是公开的；
		但随着密码长度的增加，可能的密码数量也会增加，使得攻击时间延长。</p>

		<br /><hr /><br />

		<h3>目标</h3>
		<p>你的目标通过暴力穷举获取到管理员的密码。
		如果你能获取到其他用户的密码还有隐藏奖励！</p>

		<br /><hr /><br />

		<h3>低级别</h3>
		<p>开发者完全遗漏了<u>任何保护方法</u>，允许任何人随心所欲地多次尝试，登录任何用户而不受到任何影响。</p>

		<br />

		<h3>中级别</h3>
		<p>这个级别在登录失败的屏幕上增加了一个睡眠。这意味着当你错误地登录时，在页面可见之前会有一个额外的两秒等待。</p>

		<p>这只会减慢每分钟可处理的请求量，使暴力攻击的时间更长。</p>

		<br />

		<h3>高级别</h3>
		<p>有一个“反跨站请求伪造（CSRF）令牌”被使用。有个古老传说，这种保护可以阻止暴力穷举，但事实并非如此。
		这个级别也是在中级别上的延伸，在登录失败时等待，但这次是在2到4秒之间的随机时间。
		这样做的是试图混淆任何时间预测。</p>

