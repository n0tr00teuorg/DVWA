		<br />

		<h3>不可能级别</h3>
		<p>在不可能级别上，暴力破解（和用户列举）应该是不可能的。开发者增加了一个“锁定”功能，如果过去15分钟内有5次失败登录，被锁定的用户就不能再登录。</p>

		<p>如果被锁定的用户试图登录，即使有一个有效的密码，它也会说他们的用户名或密码不正确。这将使我们无法知道系统中是否有一个有效的账户，是否有该密码，以及该账户是否被锁定。</p>

		<p>这可能导致 “拒绝服务”（DoS），因为有人不断地试图登录某人的账户。
		这个级别需要通过将攻击者列入黑名单来扩展（如IP地址、国家/地区、用户代理）。</p>
